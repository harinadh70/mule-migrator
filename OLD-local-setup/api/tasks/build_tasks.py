_DEFAULT_POM_TEMPLATE = """\
<?xml version="1.0" encoding="UTF-8"?>
<project xmlns="http://maven.apache.org/POM/4.0.0"
         xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance"
         xsi:schemaLocation="http://maven.apache.org/POM/4.0.0
                             https://maven.apache.org/xsd/maven-4.0.0.xsd">
    <modelVersion>4.0.0</modelVersion>

    <parent>
        <groupId>org.springframework.boot</groupId>
        <artifactId>spring-boot-starter-parent</artifactId>
        <version>3.2.5</version>
        <relativePath/>
    </parent>

    <groupId>{group_id}</groupId>
    <artifactId>{artifact_id}</artifactId>
    <version>0.0.1-SNAPSHOT</version>
    <name>{project_name}</name>
    <description>Auto-generated Spring Boot project from MuleSoft migration</description>

    <properties>
        <java.version>{java_version}</java.version>
    </properties>

    <dependencies>
        <dependency>
            <groupId>org.springframework.boot</groupId>
            <artifactId>spring-boot-starter-web</artifactId>
        </dependency>
        <dependency>
            <groupId>org.springframework.boot</groupId>
            <artifactId>spring-boot-starter-actuator</artifactId>
        </dependency>
        <dependency>
            <groupId>org.springframework.boot</groupId>
            <artifactId>spring-boot-starter-test</artifactId>
            <scope>test</scope>
        </dependency>
    </dependencies>

    <build>
        <plugins>
            <plugin>
                <groupId>org.springframework.boot</groupId>
                <artifactId>spring-boot-maven-plugin</artifactId>
            </plugin>
        </plugins>
    </build>
</project>
"""


def _generate_pom(
    project_dir: Path,
    group_id: str = "com.example",
    artifact_id: str = "migrated-app",
    project_name: str = "Migrated Application",
    java_version: str = "17",
) -> None:
    """Write a fallback pom.xml if one wasn't generated by the agents."""
    pom_path = project_dir / "pom.xml"
    if not pom_path.exists():
        content = _DEFAULT_POM_TEMPLATE.format(
            group_id=group_id,
            artifact_id=artifact_id,
            project_name=project_name,
            java_version=java_version,
        )
        pom_path.write_text(content, encoding="utf-8")
        logger.info("build.pom_generated", path=str(pom_path))


# ---------------------------------------------------------------------------
#  File extraction
# ---------------------------------------------------------------------------


def _extract_files(output_files: Dict[str, str], target_dir: Path) -> int:
    """
    Write generated source files into the target directory.

    Args:
        output_files: Mapping of relative filepath -> file content.
        target_dir: Root directory to write files into.

    Returns:
        Number of files written.
    """
    count = 0
    for rel_path, content in output_files.items():
        # Sanitize path to prevent directory traversal
        safe_path = Path(rel_path).parts
        if ".." in safe_path:
            logger.warning("build.skipped_unsafe_path", path=rel_path)
            continue

        full_path = target_dir / Path(*safe_path)
        full_path.parent.mkdir(parents=True, exist_ok=True)
        full_path.write_text(content, encoding="utf-8")
        count += 1

    return count


# ---------------------------------------------------------------------------
#  Maven build execution
# ---------------------------------------------------------------------------


def _run_maven_build(
    project_dir: Path,
    build_id: str,
) -> tuple[int, str]:
    """
    Run ``mvn clean package -DskipTests`` and stream output to Redis.

    Returns:
        A tuple of (exit_code, full_build_log).
    """
    maven_cmd = shutil.which("mvn") or "mvn"
    cmd = [
        maven_cmd,
        "clean",
        "package",
        "-DskipTests",
        "-Dmaven.test.skip=true",
        "-Dmaven.compiler.failOnError=false",
        "-B",  # batch mode (no interactive input)
        "--no-transfer-progress",
    ]

    log_lines: list[str] = []
    line_number = 0

    try:
        process = subprocess.Popen(
            cmd,
            cwd=str(project_dir),
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            text=True,
            env={**os.environ, "JAVA_HOME": os.getenv("JAVA_HOME", "")},
        )

        for line in iter(process.stdout.readline, ""):
            line = line.rstrip("\n")
            log_lines.append(line)
            line_number += 1

            # Stream each line to Redis pub/sub
            _run_async(_publish_build_event(build_id, {
                "type": "build_output",
                "build_id": build_id,
                "line": line,
                "line_number": line_number,
                "timestamp": time.time(),
            }))

        process.wait()
        exit_code = process.returncode

    except FileNotFoundError:
        error_msg = "Maven (mvn) not found on PATH. Please install Maven."
        log_lines.append(error_msg)
        _run_async(_publish_build_event(build_id, {
            "type": "build_output",
            "build_id": build_id,
            "line": error_msg,
            "line_number": line_number + 1,
            "is_error": True,
            "timestamp": time.time(),
        }))
        exit_code = 127

    except Exception as exc:
        error_msg = f"Build process error: {exc}"
        log_lines.append(error_msg)
        exit_code = 1

    full_log = "\n".join(log_lines)
    return exit_code, full_log


