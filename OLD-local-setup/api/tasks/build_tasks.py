#  Task: run_build
# ---------------------------------------------------------------------------


class BuildTask(Task):
    """Custom base task with retry for transient failures."""

    autoretry_for = (ConnectionError, OSError)
    retry_backoff = True
    retry_backoff_max = 60
    retry_jitter = True
    max_retries = 2


@celery_app.task(
    bind=True,
    base=BuildTask,
    name="api.tasks.build_tasks.run_build",
    acks_late=True,
    reject_on_worker_lost=True,
    track_started=True,
)
def run_build(
    self,
    build_id: str,
    migration_id: str,
) -> Dict[str, Any]:
    """
    Build generated Spring Boot code with Maven.

    Extracts files from the MigrationJob's output, generates a pom.xml if
    missing, runs Maven, and records the result in the BuildJob row.

    Args:
        build_id: UUID of the BuildJob row.
        migration_id: UUID of the parent MigrationJob.

    Returns:
        A dict with build status, exit code, and duration.
    """
    logger.info(
        "build.task_started",
        build_id=build_id,
        migration_id=migration_id,
        task_id=self.request.id,
    )

    tmp_dir = None
    try:
        result = _run_async(_execute_build(self, build_id, migration_id))
        return result
    except SoftTimeLimitExceeded:
        logger.error("build.soft_timeout", build_id=build_id)
        _run_async(_mark_build_finished(build_id, exit_code=124, log="Build timed out"))
        _run_async(_publish_build_event(build_id, {
            "type": "build_failed",
            "build_id": build_id,
            "reason": "timeout",
            "timestamp": time.time(),
        }))
        return {"status": "timed_out", "build_id": build_id}
    except Exception as exc:
        logger.error(
            "build.task_error",
            build_id=build_id,
            error=str(exc),
            exc_info=True,
        )
        _run_async(_mark_build_finished(build_id, exit_code=1, log=str(exc)))
        _run_async(_publish_build_event(build_id, {
            "type": "build_failed",
            "build_id": build_id,
            "error": str(exc),
            "timestamp": time.time(),
        }))
        raise self.retry(exc=exc)


async def _execute_build(
    task: Task,
    build_id: str,
    migration_id: str,
) -> Dict[str, Any]:
    """Core async logic for a build run."""
    from api.models.build import BuildJob
    from api.models.migration import MigrationJob

    # ── Load MigrationJob to get output files ──────────────────
    session = await _get_session()
    try:
        mig_result = await session.execute(
            select(MigrationJob).where(MigrationJob.id == migration_id)
        )
        migration = mig_result.scalar_one_or_none()
        if migration is None:
            raise ValueError(f"MigrationJob {migration_id} not found")

        output_files: Dict[str, str] = migration.output_files or {}
        if not output_files:
            raise ValueError(f"MigrationJob {migration_id} has no output files to build")

        project_name = migration.project_name
        group_id = migration.group_id
        java_version = migration.java_version

        # Mark build as running
        build_result = await session.execute(
            select(BuildJob).where(BuildJob.id == build_id)
        )
        build_job = build_result.scalar_one_or_none()
        if build_job is None:
            raise ValueError(f"BuildJob {build_id} not found")

        build_job.mark_running()
        await session.commit()
    except Exception:
        await session.rollback()
        raise
    finally:
        await session.close()

    # ── Publish build_started event ────────────────────────────
    await _publish_build_event(build_id, {
        "type": "build_started",
        "build_id": build_id,
        "migration_id": migration_id,
        "project_name": project_name,
        "file_count": len(output_files),
        "timestamp": time.time(),
    })

    # ── Extract files to temp directory ────────────────────────
    tmp_dir = Path(tempfile.mkdtemp(prefix=f"build_{build_id[:8]}_"))
    try:
        file_count = _extract_files(output_files, tmp_dir)
        logger.info(
            "build.files_extracted",
            build_id=build_id,
            count=file_count,
            dir=str(tmp_dir),
        )

        # Generate pom.xml if not present
        artifact_id = project_name.lower().replace(" ", "-")
        _generate_pom(
            tmp_dir,
            group_id=group_id,
            artifact_id=artifact_id,
            project_name=project_name,
            java_version=java_version,
        )

        # ── Run Maven build ────────────────────────────────────
        start_time = time.monotonic()
        exit_code, full_log = _run_maven_build(tmp_dir, build_id)
        duration_ms = int((time.monotonic() - start_time) * 1000)

        # ── Update BuildJob in DB ──────────────────────────────
        await _mark_build_finished(
            build_id,
            exit_code=exit_code,
            log=full_log,
        )

        # ── Publish completion event ───────────────────────────
        status = "completed" if exit_code == 0 else "failed"
        await _publish_build_event(build_id, {
            "type": "build_complete" if exit_code == 0 else "build_failed",
            "build_id": build_id,
            "migration_id": migration_id,
            "exit_code": exit_code,
            "status": status,
            "duration_ms": duration_ms,
            "timestamp": time.time(),
        })

        logger.info(
            "build.task_completed",
            build_id=build_id,
            exit_code=exit_code,
            duration_ms=duration_ms,
        )

        return {
            "status": status,
            "build_id": build_id,
            "migration_id": migration_id,
            "exit_code": exit_code,
            "duration_ms": duration_ms,
        }
    finally:
        # ── Clean up temp directory ────────────────────────────
        try:
            shutil.rmtree(tmp_dir, ignore_errors=True)
            logger.debug("build.tmp_cleaned", dir=str(tmp_dir))
        except Exception as exc:
            logger.warning(
                "build.tmp_cleanup_failed",
                dir=str(tmp_dir),
                error=str(exc),
            )


