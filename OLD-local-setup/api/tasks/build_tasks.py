async def _mark_build_finished(
    build_id: str,
    exit_code: int,
    log: str = "",
) -> None:
    """Update the BuildJob row with final results."""
    from api.models.build import BuildJob

    session = await _get_session()
    try:
        result = await session.execute(
            select(BuildJob).where(BuildJob.id == build_id)
        )
        job = result.scalar_one_or_none()
        if job:
            job.build_log = log[-50_000:]  # truncate to 50 KB
            job.mark_completed(exit_code)
            await session.commit()
    except Exception:
        await session.rollback()
    finally:
        await session.close()
