def _run_async(coro):
    """Run an async coroutine from synchronous Celery task context."""
    try:
        loop = asyncio.get_event_loop()
        if loop.is_running():
            import concurrent.futures
            with concurrent.futures.ThreadPoolExecutor() as pool:
                future = pool.submit(asyncio.run, coro)
                return future.result()
        else:
            return loop.run_until_complete(coro)
    except RuntimeError:
        return asyncio.run(coro)


async def _get_session():
    """Create a standalone async session for use inside Celery workers."""
    from api.database import _get_session_factory
    factory = _get_session_factory()
    return factory()


async def _get_redis():
    """Create a standalone Redis connection for Celery workers."""
    import redis.asyncio as aioredis
    from api.config import get_settings
    settings = get_settings()
    return aioredis.from_url(settings.redis.url, decode_responses=True)


async def _publish_build_event(build_id: str, event: Dict[str, Any]) -> None:
    """Publish a build event to the Redis channel."""
    redis = await _get_redis()
    try:
        channel = f"build:{build_id}:output"
        await redis.publish(channel, json.dumps(event))

        # Store for replay
        event_key = f"build:{build_id}:event_log"
        score = event.get("timestamp", time.time())
        await redis.zadd(event_key, {json.dumps(event): score})
        await redis.zremrangebyrank(event_key, 0, -1001)
        await redis.expire(event_key, 86400)
    finally:
        await redis.close()


